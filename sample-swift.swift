// Initialize Array with 5 elements
var myArray = [1, 2 , 3, 4, 5]
// Remove all elements from an array
myArray.removeAll()
// Check if Array is empty
if myArray.isEmpty {
    print("Array is empty")
} else {
    print("Array is not empty")
}
// Another way to check if Array is empty
if myArray.count > 0 {
    print("Array is empty")
} else {
    print("Array is not empty")
}
